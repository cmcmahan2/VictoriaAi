export const PURCHASE_AGREEMENT_TEMPLATE = `REAL ESTATE PURCHASE AND SALE AGREEMENT
(Wholesale / Assignable)

DISCLAIMER: This is a generic template for educational purposes only. It is NOT
legal advice. Real estate contracts are governed by state-specific law. Have a
licensed attorney or title company in the property's state review and adapt this
document before signing or presenting it to a seller.

------------------------------------------------------------------------------

1. PARTIES
   This Agreement is made on _______________ (date) between:
   Seller: _________________________________________________
   Buyer:  _________________________________________________ ("Buyer", and/or
   Buyer's assigns).

2. PROPERTY
   The Seller agrees to sell and the Buyer agrees to buy the real property
   located at:
   Address: ________________________________________________
   City/State/ZIP: _________________________________________
   Parcel / APN: ___________________________________________
   together with all improvements and fixtures ("the Property").

3. PURCHASE PRICE
   Total purchase price: $________________________
   Earnest money deposit: $______________ to be held by ______________________
   (title company / escrow agent), applied to the purchase price at closing.

4. CLOSING
   Closing shall occur on or before _______________ (date) at a title company
   or closing agent selected by the Buyer. Seller shall convey marketable title
   by general warranty deed, free of liens except those agreed in writing.

5. ASSIGNMENT
   Buyer may assign this Agreement, and all rights and obligations under it, to
   any third party or entity without further consent of the Seller. Upon
   assignment, the assignee assumes all of the Buyer's obligations and the
   original Buyer is released. (Seller acknowledges Buyer may be a real estate
   wholesaler and may profit from assignment of this contract.)

6. INSPECTION / DUE DILIGENCE PERIOD
   Buyer shall have _______ days from the effective date to inspect the Property.
   Buyer may terminate this Agreement for any reason during this period and the
   earnest money shall be returned.

7. CONDITION
   The Property is sold AS-IS. Seller makes no warranty as to condition except
   as expressly stated herein.

8. DEFAULT
   If Seller defaults, Buyer may seek specific performance or a refund of the
   earnest money. If Buyer defaults, the earnest money may be retained by Seller
   as liquidated damages.

9. ENTIRE AGREEMENT
   This document is the entire agreement between the parties and may be modified
   only in writing signed by both parties.

SIGNATURES

Seller: _______________________________   Date: _______________
Buyer:  _______________________________   Date: _______________
`;

